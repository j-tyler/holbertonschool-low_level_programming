/**
 * op_add - add args
 * @a: int 1
 * @b: int 2
 *
 * Return: sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub tract b from a
 * @a: int 1
 * @b: int 2
 *
 * Return: difference of a minus b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply a by b
 * @a: int 1
 * @b: int 2
 *
 * Return: a times b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a by b
 * @a: int 1
 * @b: int 2
 *
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return the remainder of the division of a by b
 * @a: int 1
 * @b: int 2
 *
 * Return: remainder of div a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
